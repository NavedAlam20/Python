# Accessing Tuple with Indexing
tup = tuple("Python")
print(tup[0]) #P

# Accessing a range of elements using slicing
print(tup[1:4]) #yth
print(tup[:3]) #Pyt

# Tuple unpacking
tup1 = ("Welcome", "To", "Python")

# This line unpack values of Tuple1
a, b, c = tup1
print(a) #Welcome
print(b) #To
print(c) #Python