tup = (5, 'Welcome', 7, 'python')
print(tup) #(5, 'Welcome', 7, 'python')

# Creating a Tuple with nested tuples
tup1 = (0, 1, 2, 3)
tup2 = ('python', 'python')
tup3 = (tup1, tup2)
print(tup3) #((0, 1, 2, 3), ('python', 'python'))

# Creating a Tuple with repetition
tup1 = ('python',) * 3
print(tup1) #('python', 'python', 'python')